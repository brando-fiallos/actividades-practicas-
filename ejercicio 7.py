
#declaracion de variables 
entero = 10  # Variable entera
flotante = 5.5  # Variable flotante
cadena = "Hola, Brando!"  # Variable de tipo cadena
booleano = True  # Variable booleana


print("Entero:", entero)
print("Flotante:", flotante)
print("Cadena:", cadena)
print("Booleano:", booleano)


#operaciones numericas 
a = 8
b = 4
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b  
print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)


# Operadores lógicos
x = True
y = False
print("x and y:", x and y)  # Devuelve False porque ambos no son True
print("x or y:", x or y)  # Devuelve True porque al menos uno es True
print("not x:", not x)  # Invierte el valor de x, devolviendo False
print("not y:", not y)  # Invierte el valor de y, devolviendo True